
# basic for loop using range function
# range(from-inclusive,up to-exclusive,step)
for i in range(10):
    print("hello")

# basic for loop iterating over a list and printing each element
words = ['one', 'two', 'three', 'four', 'five']

for i in words:
    print(i)

# using for loop with tuple
animals = ( 'bear', 'bunny', 'dog', 'cat', 'velociraptor' )

for pet in animals:
    print(pet)
